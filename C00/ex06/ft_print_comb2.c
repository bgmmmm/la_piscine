#include <unistd.h>

void	ft_printarr(char *a)
{
	if ((a[0] == a[3] && a[1] < a[4]) || (a[0] < a[3]))
	{
		write(1, a, 5);
		if (!(a[0] == '9' && a[1] == '8' && a[3] == '9' && a[4] == '9'))
		{
			write(1, ", ", 2);
		}
	}
}

void	ft_print_comb2(void)
{
	char	arr[6];

	arr[0] = '0';
	arr[2] = ' ';
	while (arr[0] <= '9')
	{
		arr[1] = '0';
		while (arr[1] <= '9')
		{
			arr[3] = '0';
			while (arr[3] <= '9')
			{
				arr[4] = '0';
				while (arr[4] <= '9')
				{
					ft_printarr(arr);
					arr[4]++;
				}
				arr[3]++;
			}
			arr[1]++;
		}
		arr[0]++;
	}
}

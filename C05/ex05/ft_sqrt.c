int	ft_sqrt(int nb)
{
	long long	i;

	if (nb <= 0)
		return (0);
	i = 1;
	while (i * i < nb)
	{
		i++;
	}
	if (i * i == nb)
		return ((int)i);
	else
		return (0);
}

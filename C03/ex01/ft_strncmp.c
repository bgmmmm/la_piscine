int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	if (n == 0)
		return (0);
	while (n > 0 && *s1 != 0 && *s2 != 0)
	{
		if (*s1 == *s2)
		{
			n--;
			if (n == 0)
				break ;
			s1++;
			s2++;
		}
		else
			break ;
	}
	return (*s1 - *s2);
}
